#include "libft.h"

void	ft_strrev(char *s)
{
	char tmp;
	char *rev;

	rev = s + ft_strlen(s) - 1;
	while (s < rev)
	{
		tmp = *s;
		*s = *rev;
		*rev = tmp;
		s++;
		rev--;
	}
}
